# Ejercicio 4:
def contar_vocales(palabra):
    vocales = "aeiouáéíóúAEIOUÁÉÍÓÚ"
    return sum(1 for letra in palabra if letra in vocales)

def contar_consonantes(palabra):
    return sum(1 for letra in palabra if letra.isalpha() and letra.lower() not in "aeiouáéíóú")

def analizar_oraciones():
    oracion = input("Ingrese una o más oraciones: ")
    palabras = oracion.split()

    total_vocales = 0
    total_consonantes = 0

    for palabra in palabras:
        total_vocales += contar_vocales(palabra)
        total_consonantes += contar_consonantes(palabra)

    print(f"Total de vocales: {total_vocales}")
    print(f"Total de consonantes: {total_consonantes}")



# Ejecicio 1:valor maximo de tres.
def maximo_de_tres(a, b, c):
    return max(a, b, c)

# Ejercicio 2:valor maximo de diez.
def maximo_de_diez():
    numeros = []
    print("Ingrese 10 números:")
    for i in range(10):
        n = int(input(f"Número {i+1}: "))
        numeros.append(n)

    maximo = numeros[0]
    for i in range(1, 10, 3):
        x = numeros[i] if i < 10 else 0
        y = numeros[i+1] if i+1 < 10 else 0
        z = numeros[i+2] if i+2 < 10 else 0
        maximo = max(maximo, maximo_de_tres(x, y, z))

    print(f"El valor máximo es: {maximo}")


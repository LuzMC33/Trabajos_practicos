def cargar_matriz(filas, columnas):
    matriz = []
    print(f"Ingrese los elementos de la matriz de {filas}x{columnas}:")
    for i in range(filas):
        fila = []
        for j in range(columnas):
            valor = int(input(f"M[{i}][{j}]: "))
            fila.append(valor)
        matriz.append(fila)
    return matriz

def mostrar_matriz(m):
    for fila in m:
        print(fila)

def sumar_matrices(a, b):
    return [[a[i][j] + b[i][j] for j in range(len(a[0]))] for i in range(len(a))]

def multiplicar_matrices(a, b):
    return [[a[i][j] * b[i][j] for j in range(len(a[0]))] for i in range(len(a))]

def operaciones_con_matrices():
    m = int(input("Filas: "))
    n = int(input("Columnas: "))
    print("Matriz A:")
    A = cargar_matriz(m, n)
    print("Matriz B:")
    B = cargar_matriz(m, n)

    print("¿Qué operación desea realizar?")
    print("1 - Suma")
    print("2 - Producto")
    op = int(input("Opción: "))

    if op == 1:
        C = sumar_matrices(A, B)
        print("Resultado (A + B):")
    elif op == 2:
        C = multiplicar_matrices(A, B)
        print("Resultado (A * B):")
    else:
        print("Opción inválida.")
        return

    mostrar_matriz(C)

